public class BarangArray {
    String nama;
    int stok;

    public BarangArray(String nama, int stok) {
        this.nama = nama;
        this.stok = stok;
    }


    public String getNama() {
        return nama;
    }

    public int getStok() {
        return stok;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public void setStok(int stok) {
        this.stok = stok;
    }
}
